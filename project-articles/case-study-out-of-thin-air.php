                        <p class="tpr-article-content__paragraph">tpr media consultants were engaged from May 2017 by Mosaic Films to create and deliver a PR strategy for the
                        documentary Out of Thin Air. We positioned the feature-length film as a compelling true crime story, a successor to
                        Making a Murderer with stylish, Nordic Noir appeal. The campaign tied in with its broadcast on BBC Storyville (pushed
                        back from July to August) and Netflix availability (from September).</p>
                        <p class="tpr-article-content__paragraph">For the film to stand out with the level of recognition and awareness it deserved required careful PR positioning and
                        implementation. The first challenge was to make this story accessible in words and images. With the support of Mosaic
                        Films, tpr media spent a lot of time researching the background to the case and creating a press pack and website which
                        told the story, painting a picture of 1970s Iceland, the backstory of the contributors and an understanding of Memory
                        Distrust Syndrome, a rare condition where people lose trust in their own memories. We felt this this was essential
                        groundwork in helping an already overstretched media to understand the complex but fascinating case. We also focused on
                        the stylised look and feel of the documentary and the trailer was very helpful here. Using these finely-tuned media
                        materials as a starting point, we created different pitches for different outlets, carefully briefing contributors –
                        especially Erla Bolladottir and Dr Gisli Gudjonsson – and the production team, who we were in regular contact with.</p>

                        <p class="tpr-article-content__paragraph">Knowing that Out of Thin Air would appeal to a younger-than-usual Storyville audience, we aimed for coverage in a broad
                        range of outlets that included the Guardian, Radio 4’s Woman’s Hour and The World Tonight as well as Vice – where it
                        was one of their most shared stories of the week – film website Little White Lies and the Huffington Post. BBC
                        Newsnight were keen to cover the story but bumped it twice due to a busy news agenda. We had blanket previews, many
                        four and five stars, from the Financial Times to the Observer; the listings magazines also lapped it up and Broadcast
                        ran a behind the scenes feature written by the production team. Overall there were 52 pieces of coverage with a total
                        reach of 13.3million people. The total news value for the campaign was £404, 850.</p>
                        
