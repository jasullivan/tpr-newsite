                        <p class="tpr-article-content__paragraph">tpr spent a very cold, but very exciting, day on the set of the new Channel 4 drama The Mill (w/t) yesterday. Set in the turbulent times of 1833, The Mill (w/t) looks at the Industrial Revolution through the eyes of the young apprentices working in Quarry Bank Mill, owned by the Gregs – one of the largest "cotton king" families in Britain.</p>

                        <p class="tpr-article-content__paragraph">Filmed in the real Quarry Bank Mill in St, Cheshire, and based on the extensive historical archives of the mill, many of the characters are based on real apprentices – in particular Esther Price, a feisty Liverpudlian girl who is the driving force of the show.</p>

                        <p class="tpr-article-content__paragraph">Despite the cold we had a fantastic day on set, watching filming and being shown the Mule spinning machines. The production director and her team created five Mules – exact replicas of the machines that would have been used at Quarry Bank Mill in 1833. Three of these Mules work, and are able to spin cotton into thread. The team created them in just a few months, with only a sketch to work from – before these were built, no Mules of that era existed in the UK. The experts that came on board to help said they were insane, but the impressive Mule Room on set proves them wrong!</p>

                        <p class="tpr-article-content__paragraph">We had a chance to meet the cast of the show, which features some famous faces as well as some up-and-coming actors. Donald Sumpter is Samuel Greg, the mill owner, while Mr Timperley, the grumpy mill overseer is played by Kevin McNally.</p>

                        <p class="tpr-article-content__paragraph">But the apprentices are really the focus of the show. Esther Price is played by Kerrie Hayes, and pregnant Susannah is played by Holly Lucas. Sacha Parkinson, who starred in Coronation Street for two years, plays a young apprentice called Miriam. They say that filming in Quarry Bank, where the actual events took place really helped them get into character, as did their research into the tough lives of the apprentices. As Holly Lucas told us, "There was an idea of freedom but you couldn't even go for a walk." For her 26<sup>th</sup> birthday – which happened during filming – Kerrie Hayes was even given a copy of the real Esther Price's birth certificate and contract of employment at Quarry Bank Mill.</p>

                        <p class="tpr-article-content__paragraph">The Mill (w/t) is the result of the first full collaboration between Channel 4's factual and drama departments. It is scheduled to air in summer 2013 (tbc).</p>

