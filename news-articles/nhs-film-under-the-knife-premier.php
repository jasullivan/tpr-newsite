                        <p class="tpr-article-content__paragraph">Last Thursday the world premiere of Under the Knife took place at the Curzon Containers, South Bank. The feature-length documentary traces the history of the NHS from its conception in 1948 post-war Britain to modern day. From its stormy birth, through seven decades of turmoil and political warfare, it has withstood almost everything that has confronted it – until now.</p>

                        <p class="tpr-article-content__paragraph">The film began in 2014 as a passion project for producer Pam Kleinot, who was working in the NHS and became increasingly alarmed by what she saw. "I experienced the surveillance, targets for staff and uncertainty that resulted from funding increasingly being cut," she said. It was this that spurred her on to make the film. What started out as a personal project grew into a four-year investigation resulting in this 90-minute film made with Emmy award-winning director Susan Steinberg and narrated by actor Alison Steadman, who joined us at the premiere and described Under the Knife as "an inspiring film that...gives us courage."</p>

                        <p class="tpr-article-content__paragraph">But Pam wants this film to be more than an interesting watch – she wants it to have impact. That’s why she's joined forces with Keep Our NHS Public (KONP) the campaigning organisation that in March saved Ealing and Charing Cross hospitals. A successful crowdfunding campaign with KONP and a number of trade unions has meant that Under the Knife will be screened in over 50 venues free of charge – locations include London, Manchester, Birmingham, Newcastle, Liverpool, and Brighton from 14-18 October. To book free tickets visit: <strong><a href="https://undertheknifefilm.co.uk/screenings/" target="_blank">undertheknifefilm.co.uk/screenings.</a></strong></p>

                        <p class="tpr-article-content__paragraph">Award-winning filmmaker, Ken Loach, a big supporter of the film, attended the premiere along with Alison Steadman, Aneira Thomas, the first-ever baby to be born on the NHS, as well as Michael Mansfield QC who features in the film.</p>

