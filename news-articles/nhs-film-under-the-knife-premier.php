                        <p class="tpr-article-content__paragraph">We’re thrilled that the Daily Mirror, with their track-record of ground-breaking investigative journalism, has come on board as our media partner. On Thursday night, Associate Editor, Kevin Maguire, chaired a provocative Q&A with Pam Kleinot, Susan Steinberg and Tony O’Sullivan, co-chair Keep Out NHS Public. Journalist Paul Routledge wrote this article setting the scene for the film’s release <a href="https://www.mirror.co.uk/news/politics/how-tories-plotted-decades-sell-20102171" target="_blank"><strong> www.mirror.co.uk/news/politics/how-tories-plotted-decades-sell-20102171</strong></a></p>

                        <p class="tpr-article-content__paragraph">Ken Loach was unequivocal about the film: "Films, like words, can be weapons. This film is a weapon in our struggle to save the NHS. There should be details of the film in every hospital reception, every GP's waiting room, every community centre."</p>

                        <p class="tpr-article-content__paragraph">Under the Knife ends on hope, illustrating how communities, health care professionals and campaigners have effectively fought to defend hospitals and services threatened with closure through the courts, in council chambers and on the streets.</p>

                        <p class="tpr-article-content__paragraph">Tony O’Sullivan added: "This is the best film around on the NHS. Under the Knife shows the vital importance of the NHS to society but most important of all, the film gives hope to those who are campaigning to keep the NHS safe for our children. You just have to see it."</p>

