                        <p class="tpr-article-content__paragraph">Recently we were busy at London Film Festival, as we publicised Drones, a film that takes an unflinching examination of the moral issues and collateral damage facing a U.S. military that relies ever more heavily on the use of unmanned drones to execute its strategies.</p>

                        <p class="tpr-article-content__paragraph">It is such a powerful film, with a timely look at the future of warfare, which focuses ever more heavily on the use of unmanned drones to execute its strategies. Due to the film's topical message we got in touch and invited a number of drones activist to the screening – we hope that it raises awareness surrounding the controversies of the use of drones and would like to wish it the best of luck as it continues to screen around the world.</p>

                        <p class="tpr-article-content__paragraph">Bedlam – which goes out tonight Channel 4 at 9pm – takes a fly on the wall look at the reality of the mental health care in Britain. The series provides a voice to those with mental illness, from people with psychosis to those with severe anxiety. Although a growing number, 1 in 4, of us will experience mental illness in our lifetime, the stigma remains: it's an unwanted label.  This stigma is still seen in the mainstream media, the most recent example, a sensationalist front cover of The Sun, which caused controversy on the 7th October, as well as Asda and Tesco’s "mental patient" Halloween costume.</p>

                        <p class="tpr-article-content__paragraph">Bedlam looks at challenging these stereotypes by following the lives of patients and their families, touching on a range of mental health conditions. We also hear from the staff who work with the patients on their path to recovery, many of which do in fact get better. Filmed over a year, each of the four films tackles a different aspect of mental health.</p>

                        <p class="tpr-article-content__paragraph">On Monday at a Channel 4 screening, it was really inspiring to hear the positive feedback from the contributors. Many explained that they participated in order to challenge the surrounding stigma but to also spread awareness amongst those who may also have mental illness. They supported the series suggesting that it not only challenge the stigma that they face but it does it with humanity and humour.</p>

                        <p class="tpr-article-content__paragraph">Additionally we learnt about the power of compassion last Thursday at this year's Empathy and Compassion conference, of which we are one of the sponsors. The day offered a refreshing look at the way in which we can improve our approach to work, to be more compassionate towards others as well as ourselves – all of which was supported with extensive scientific research.</p>

                        <p class="tpr-article-content__paragraph">We heard from an A-list line-up of speakers including Paul Ekman – one of the world's leading clinical psychologists and pioneer in the study of emotions – who has revisited Darwin, suggesting that he expressed compassion to be as important as competition. As well as author Adam Grant who argues that many of us value giving in our personal lives, but fear that if we help others at work, we’ll sacrifice our own success. Kristin Neff conducted a workshop teaching the basics of self-compassion which she suggests it is as a beneficial alternative to self-esteem which, in a society which looks to achieve above average, often results in self-loathing – particularly in women – or narcissism.</p>

                        <p class="tpr-article-content__paragraph">The conference was an important break from our manic working day as it offered time to reflect and put forward ideas to improve the way in which we work. We would really recommend that you take a look at the conferences’ <a class="tpr-article-content__link" href="http://compassioninsociety.org/" target="_blank" rel="noopener">website</a> and follow the speakers work – as it offers a thoughtful and alternative perspective.</p>

                        

                        

