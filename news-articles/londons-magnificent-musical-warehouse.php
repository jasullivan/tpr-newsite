                        <p class="tpr-article-content__paragraph">James Rhodes' Don’t Stop The Music instrument amnesty – the nationwide campaign to put musical instruments into the hands of primary school children around the UK – closed it's appeal for musical instruments last month.</p>

                        <p class="tpr-article-content__paragraph">It's only now that the scale of the public’s incredible response can really be judged and the good news is that it makes all the hard work of everyone involved (from Oxfam and Yodel to Yamaha and Universal Music) worth while.</p>

                        <p class="tpr-article-content__paragraph">The final count is still being added up but so far over 6000 instruments have been delivered to a West London warehouse where they're awaiting delivery to primary schools across the country where they can really make a difference.</p>

                        <p class="tpr-article-content__paragraph">Donations range from accordions to zithers, including French horns, cellos, clarinets, violins, drumkits, and much more besides. It's really an awe-inspiring sight to see, reminiscent of the memorable army warehouse camera scan at the conclusion of Raiders of the Lost Ark (albeit significantly less sinister).</p>

