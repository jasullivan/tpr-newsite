                        <p class="tpr-article-content__paragraph">For the thousands of children who'll be receiving an instrument of their own, in many cases for the first time, a comparison to Santa's workshop might be more appropriate. Not least because there’s a team of volunteers (including expert repairers and talented music specialists) working hard to give each instrument a new lease of life.</p>

                        <p class="tpr-article-content__paragraph">In addition to these acts of generosity, what's also been touching is the heart-warming notes of support that have come with many of the instruments. One reads: "This instrument taught me maths, literature, how to make friends and much, much more. What I learned enabled me to teach myself the guitar, which I am now teaching my twin daughters in Australia. You will get frustrated at times, but don’t give up! Have fun."</p>

                        <p class="tpr-article-content__paragraph">The campaign's been a fantastic project to work on, and one that we really hope will make a difference. It's been great to see Don't Stop The Music mentioned extensively in Parliament this month, in extremely positive terms. Meanwhile the support of the press and celebrities, alongside the public, has given the project a massive boost.</p>

                        <p class="tpr-article-content__paragraph">Impassioned responses included Tom Service from the Guardian: "Rhodes rightly says that his initiative is designed to fill the gaps left in this government’s – and the last one, and the one before that's... – failure to meaningfully live up to their well-intentioned but, in practice, hollow promises to make instrumental tuition available to every child in the country."</p>

                        <p class="tpr-article-content__paragraph">And Sir Paul McCartney: "If it hadn’t been for my dad giving me a trumpet on my 14<sup>th</sup> birthday, which I traded for a Framus Zenith guitar, I’d have never joined The Beatles. So drop your instrument at Oxfam and donate your instrument to James’ Don’t Stop The Music Instrument Amnesty. You might inspire a future rockstar!"</p>

