                        <p class="tpr-article-content__paragraph">I'm delighted to tell you about two new projects we’re working on this spring: the Great Exhibition Road Festival in South Kensington, London (28-30 June), and the UK cinema release (from 14 June) of Five Seasons: The Gardens of Piet Oudolf, a film about the visionary Dutch landscape designer who has created some of the most famous gardens in the world.</p>
                    
                        <p class="tpr-article-content__paragraph">Over one weekend this summer, South Kensington's Exhibition Road will host a free festival of art, science and culture. The Great Exhibition Road Festival will transform the area, with interactive workshops, behind-the-scenes tours, exciting talks and dynamic performances to inspire all ages.</p>

                        <p class="tpr-article-content__paragraph">The festival – a collaboration between Imperial College London, the Natural History Museum, the V&A, the Royal Albert Hall, the Science Museum, the Royal Commission for the Exhibition of 1851 and others – marks the bicentenary of the births of Queen Victoria and Prince Albert, taking inspiration from their vision for the Great Exhibition of 1851, which showcased the latest innovations in British industry and culture, and was visited by six million people. The area around Exhibition Road – 'Albertopolis' – was created from the profits of the Great Exhibition and was the first cultural centre of its kind in the world.</p>

                        <p class="tpr-article-content__paragraph">The Great Exhibition Road Festival celebrates the crossover between science and the arts and has an amazing line-up of speakers – from the first British astronaut, Helen Sharman, to scientist and broadcaster, Professor Lord Robert Winston. It includes hundreds of events – from an exhibition of the love letters of Queen Victoria and Prince Albert – in German and surprisingly passionate – to playing chess with a robot. To register free for the Great Exhibition Road Festival,  <a class="tpr-article-content__link" href="https://www.eventbrite.co.uk/e/the-great-exhibition-road-festival-2019-registration-58730217662?invite=&err=29&referrer=&discount=&affiliate=&eventpassword=#loginbox" target="_blank" rel="nofollow noopener">sign-up here</a>.</p>

                        <p class="tpr-article-content__paragraph">Five Seasons: The Gardens of Piet Oudolf is a documentary about the life and work of the most influential landscape designer of the last 50 years. Oudolf was behind New York's High Line and many other urban spaces. Closer to home, he designed the 7,000 square metre public garden for the art gallery Hauser &amp; Wirth in Somerset. Over the course of the documentary, Oudolf leads filmmaker, Thomas Piper, and his camera on a wandering journey, visiting many of his iconic works.</p>

