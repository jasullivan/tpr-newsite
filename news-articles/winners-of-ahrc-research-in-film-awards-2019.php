                        <p class="tpr-article-content__paragraph">In Faces | Voices, black women and men living in London – many originating from the same regions where Thomas worked – interpret the images of people from his photo archive through a 21st century lens. The archive has been unseen for over 100 years. Director Paul Basu (SOAS) writes about his research, and the making of Face | Voices <a class="blog-link-darkblue" href="https://www.blackhistorymonth.org.uk/article/section/author-and-historians/faces-voices/" target="_blank"><strong>here</strong></a></p>
                        
                        <p class="tpr-article-content__paragraph"><strong>Intranquilities: Voices from Haiti, winner of the Best Doctoral or Early Career Film</strong> look at the contemporary Haitian art scene. The moving and uplifting film features many Haitian artists, from writers to painters, poets to photographers, as they respond to ‘postcolonial disasters,’ such as the 2010 earthquake. We see that the spirit of defiance that led Haiti to becoming the first, and only, black-led republic, lives on in their art. You can see the trailer <a class="blog-link-darkblue" href="https://www.youtube.com/watch?v=1C6uyTyr99E&list=PLyIzwe-lwzlb-qZulsCoWdaElQuZ7JJvT&index=19" target="_blank"><strong>here</strong></a></p>

                        <p class="tpr-article-content__paragraph"><strong>The Social Media Short Award went to the innovative Life on the Move</strong> – a stop motion animation exploring the complex reasons behind migration. Director Osbert Parker said 'Migration is not always traumatic or problematic; it can be a positive experience.'</p>
                        
