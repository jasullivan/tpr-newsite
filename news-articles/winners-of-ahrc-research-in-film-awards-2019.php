                        <p class="tpr-article-content__paragraph">The awards included two public categories: The Mental Health and Wellbeing Award as well as the Inspiration Award. Three in 1,000 babies in Britain either die or become severely brain-damaged by lack of oxygen or blood flow to the brain around the time of birth. However, thanks to the ground-breaking research featured in <a class="blog-link-darkblue" href="https://www.youtube.com/watch?v=_PrZwjDJRLQ&list=PLyIzwe-lwzlb-qZulsCoWdaElQuZ7JJvT&index=10" target="_blank"><strong>The Golden Window</strong></a> – winner of the Mental Health and Wellbeing Award – there is hope for children and their families. The immersive film illustrates what doctors refer to as the 'cooling technique' which takes place during the first 72 hours of a child’s life leaving them in a state of stasis, and with the hope of saving their lives.</p>

