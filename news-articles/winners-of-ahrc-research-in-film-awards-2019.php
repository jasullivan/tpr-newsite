                        <p class="tpr-article-content__paragraph">Team tpr had a great time on Tuesday night (12 November), at the BFI Southbank where the fifth annual Research in Film Awards organised by the Arts and Humanities Research Council (AHRC) took place. These are the only awards entirely dedicated to showcasing the breadth of research in arts and humanities through film and five winners were selected from hundreds of submissions and a strong short-list.</p>

                        <p class="tpr-article-content__paragraph">Judges included Chairman of ITV, Sir Peter Bazalgette, award-winning filmmaker, Kim Longinotto, and Steve Harding-Hill, Creative Director in Commercials and Short-form at Aardman Animations.</p>

