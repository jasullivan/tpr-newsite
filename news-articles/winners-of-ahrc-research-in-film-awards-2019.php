                        <p class="tpr-article-content__paragraph"><strong>Best Research Film went to Faces&#124;Voices</strong>: the first-ever film to explore the controversial archive of Northcote Thomas from Nigeria and Sierra Leone (1909 – 1915). Northcote Thomas was the very first anthropologist to be appointed by the British Colonial Office though he was later fired.</p>

