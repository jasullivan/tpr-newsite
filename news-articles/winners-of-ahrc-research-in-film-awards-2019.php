                        <p class="tpr-article-content__paragraph"><strong>The Inspiration Award went to</strong> <a class="blog-link-darkblue" href="https://www.youtube.com/watch?v=j2ZXIiGvz6c&list=PLyIzwe-lwzlb-qZulsCoWdaElQuZ7JJvT&index=4&t=0s" target="_blank"><strong>Spirit</strong></a> – a film which explores what it takes to feel at home in a remote Himalayan village. Spirit follows Saraswati, the first educated daughter-in-law to marry into the village 20 years ago and wondered how she could ever belong. Ross Harrison and Dr Jane Dyson worked together on the film to reveal that belonging cannot be assumed, but is the slow alchemy of work, friendship, love, loss and belief.</p>

                        <p class="tpr-article-content__paragraph">The amazing evening was hosted by writer and broadcaster, Danny Leigh and Jan Dalley, Arts Editor of the Financial Times, who initiated the awards, and chaired the judging panel. A huge congratulations to all the short-listed films, and to all the winners. Can’t wait until next year.</p>
