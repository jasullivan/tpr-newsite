                        <p class="tpr-article-content__paragraph">We’re pleased to say that four documentaries that tpr recently worked on have each been nominated for the Grierson Awards 2013!</p>

                        <p class="tpr-article-content__paragraph"><a class="tpr-article-content__link" href="http://nofirezone.org/" target="_blank" rel="noopener">No Fire Zone</a>, a shocking feature documentary exposing some of the worst war crimes of the 21<sup>st</sup> century, has been nominated for ITN Source Best Documentary on Current Affairs. It is the latest representationof a culmination of three years of journalistic investigation, which containsdeeply disturbing new evidence, powerful eye witness testimony and compelling personal stories of survival in a war zone. Most of all,although it is a devastating indictment of the men responsible for the crimes it is also an exposé of the failure of the international community to prevent this catastrophe. It is a privilege to work on something of such importance and it’s amazing the impact that it has made. No Fire Zone is currently preparing for the second leg of its international tour –the highlight being India.</p>

                        <p class="tpr-article-content__paragraph">Earlier this summer we worked on Chopin Saved My Life,an hour-long documentary, directed by Bafta and Emmy award-winning director James Kent. It features Japanese teenager Momoka and Scottish music student Paul Murray to explore how Chopin’s extraordinary Ballade Number 1 brought comfort to ordinary people in distress and illustrates how classical music still penetrates deep into contemporary life, in unexpected and powerful ways.</p>

                        <p class="tpr-article-content__paragraph">Momoka is a 15-year-old pianist from Sendai, Japan, whose community was decimated by the 2011 tsunami and earthquakes. Momoka struggles to accept that so many people died when the tsunami hit her country. Hearing the Ballade was the first thingthat gave her courage again.</p>

                        <p class="tpr-article-content__paragraph">For 22-year-old Paul Murraythe piece is literally life changing. Paul grew up in Bellshill, a poor, working class suburb of Glasgow, known for its gangs, drugs and knife crime. At 15, Paul played a keyboard at school for the first time, and knew instantly that he had opened up a world of classical music that neither he nor his parents were aware of. By 16 he had taught himself the piano and at 17 he fell in love with Chopin’s Ballade Number 1 in G Minor, a piece that has obsessed him ever since. After starting his degree, Paul suffered crippling headaches and underwent a 141/2hour surgery to remove a brain tumour, following which he lost the use of his right hand. Two years later Paul lost the use of his left side and was diagnosed with MS, Ballade Number 1 help path his road to recovery. Chopin Saved My Life has also been nominated for this year’s Halo Best Arts Documentary award.</p>

                        <p class="tpr-article-content__paragraph">Additionally The Year the Town Hall Shrank, a three-part documentary series, has been nominated for Best Documentary Series. This series told the story of how one city, Stoke on Trent, coped with the impact of the largest funding cuts to local government ever imposed by central government. The documentary is a gripping and moving tale of power, competing priorities and the human cost of cuts. With intimate access to Stoke City council and its decision-makers, the series followed local politicians as they struggled to make the biggest budget cuts in peacetime as well as to trace the huge impact on local people. It is a tale of sacrifice and loss but also one of politics and manoeuvring.</p>

                        <p class="tpr-article-content__paragraph">And finally Turner prize winning artist Grayson Perry has been nominated for Documentary Presenter of the Yearfor his documentaryAll in the Best Possible Taste with Grayson Perry. As an artist Grayson has always been fascinated with the subject of taste because it tells a visual story about our interior lives.  He believes that the objects with which we choose to surround ourselves often reflect our strongest deepest feelings, whether we are aware of it or not.His fascination with taste led him to make a series of six tapestries for an exhibition and a three part television series for Channel 4. The programme followed his safari amongst the taste tribes of Britain where he gathered inspiration for his artworks. The series has already won a BAFTA, and we feel that Grayson really deserves to win this Grierson for his work – it really is a fantastic insight into 21<sup>st</sup> century Britain.All in the Best Possible Taste with Grayson Perry is still available on  <a class="tpr-article-content__link" href="http://www.channel4.com/programmes/in-the-best-possible-taste-grayson-perry/4od" target="_blank" rel="noopener">4od</a>.</p>

                        

