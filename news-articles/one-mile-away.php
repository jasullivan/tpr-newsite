                        <p class="tpr-article-content__paragraph">We’ve recently been lucky enough to be working on the PR for a feature-length documentary from acclaimed director, Penny Woolcock, called One Mile Away. A brutally honest look at the attempts of two men – Shabba (B6) and Dylan (B21) – from warring postcodes to bring peace to the streets of Birmingham. With one of the highest rates of gun crime in the country, the rivalry between The Johnsons and The Burger Bar Boys was turning certain areas of Birmingham into an inner-city warzone. Sick of the daily violence, Shabba approached Penny Woolcock and put an idea to her – to broker a peace agreement between the two gangs. Having worked with both gangs to produce her fictional hip-hop musical 1 Day in 2009, based on gang rivalry in Birmingham, Penny was seen as something as a neutral character who could act as a go-between. She approached Dylan, who she had become close with during filming, and he agreed to take part.</p>

