                        <p class="tpr-article-content__paragraph">Catherine's company, Limina Immersive, curates VR-only festivals at arts centres and cinemas throughout the UK, introducing the wider public to VR and unlocking this new medium's creative possibilities.</p>
                        <p class="tpr-article-content__paragraph">Highlights include I Saw The Future (Arte, François Vautier) in which Arthur C. Clarke, the co-writer of Stanley Kubrick's 2001: A Space Odyssey, reveals his vision of the future (it's scary how much he got right!) and Grenfell: Our Home (Parable, 59 Productions, Channel 4), an intimate portrait through the voices of its residents.</p>
                        <p class="tpr-article-content__paragraph">If predictions are correct, there is a high likelihood that within our lifetimes, VR will sit alongside mainstream cultural pastimes such as theatre, radio, books, film &amp; television. By 2020, the VR content market is forecast to take $14 billion in revenue – $1 billion more than the US movie industry currently takes at the box office (Statista).</p>
                        <p class="tpr-article-content__paragraph">Catherine Allen, CEO of Limina Immersive says, "VR is an incredible emerging art form in its own right, and it deserves to be seen by as many people as possible. There are a lot of people who have notions of VR being techy, macho, futuristic and sci fi, so they assume that it is not for them. I hope to challenge these assumptions and open our audiences' senses to an emerging new world of creative potential."</p>
