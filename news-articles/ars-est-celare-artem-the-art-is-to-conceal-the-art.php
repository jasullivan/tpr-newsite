                        <p class="tpr-article-content__paragraph">Charlie and the Chocolate Factory is a childhood favourite of so many, including us here at tpr, so it’s hard to imagine it coming to life – thankfully Artem were at hand to help Sam Mendes to create some of the more extreme moments of Roald Dahl’s imagine. Working with Marcus Hall Props, they were briefed to devising a series of experiments for the Oompa Loompas to carry out in their laboratory, and to design and build an inflatable suit that would transform Violet Beauregarde, the gum-chewing winner of one of Willy Wonka’s golden tickets, into a giant blueberry.</p>

                        <p class="tpr-article-content__paragraph">First they set to work helping the Oompa Loompas to make mischief – the experiments needed to be visually engaging with bright colours, bubbles, and smoke, but they also had to be contained in a very small area on stage with dancing action around them. The created a total of six different 'laboratory' units incorporating air and fluid pumps, with lots of tubing and crazily-shaped containers with bubbles floating from one and smoke rising gently from others to replicate the effects of the Oompa Loompas' wacky sweet manufacturing experiments.</p>

                        <p class="tpr-article-content__paragraph">The inflatable suit was more complicated to create (although no problem for Artem), as it had to fit each of the child actors playing the part of Violet Beauregarde and be self-contained under their outer costumes. It also had to be simple enough for the child actor to operate as well as being safe.</p>

                        <p class="tpr-article-content__paragraph">Laura Sindall, an Artem technician, created a couple of alternative designs that were trialled to get the best look. The air supply had to be tiny in order to be hidden, but at the same time had to be capable of ensuring full inflation of the suit. It was decided that diving components with their safety margins and long reliability record provided the ideal solution.  Additional safety controls and the use of switches meant the pneumatics can be contained in a small backpack the actor is wearing. Other safety valves ensured the suit could not over inflate and squash the child!</p>

